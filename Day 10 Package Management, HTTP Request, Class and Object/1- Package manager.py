# What is PIP ?

# PIP stands for Preferred installer program. We use pip to install different Python packages. Package
# is a Python module that can contain one or more modules or other packages. A module or modules that
# we can install to our application is a package. In programming, we do not have to write every utility
# program, instead we install packages and import them to our applications.


